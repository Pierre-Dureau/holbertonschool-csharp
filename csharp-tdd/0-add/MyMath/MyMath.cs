using System;

namespace MyMath
{
    /// <summary>
    /// Represent the Operations class
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Add 2 Integers
        /// </summary>
        /// <param name="a"> First Integer </param>
        /// <param name="b"> Second Integer </param>
        /// <returns> The sum of the two Integers </returns>
        public static int Add(int a, int b) {
            return (a + b);
        }
    }
}
